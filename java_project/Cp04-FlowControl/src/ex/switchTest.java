package ex;

public class switchTest {

	public static void main(String[] args) {

		int num = 6;
		switch (num) {

		case 1:
			System.out.println("SK");
			break;

		case 6:
			System.out.println("KT");
			break;

		case 9:
			System.out.println("LG");
			break;

		default:
			System.out.println("UNKNOWN");
			break;

		}

	}

}
