package ex;

public class ArithOp {

	public static void main(String[] args) {

		int a = 20;
		int b = 3;

		int result = a + b;
		System.out.println(result);

		result = a - b;
		System.out.println(result);

		result = a * b;
		System.out.println(result);

		result = a / b;

		System.out.println(result);

		result = a % b;
		System.out.println(result);

	}

}
